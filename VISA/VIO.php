  <div class="title"><h3>3.Что такое легализация документов и каков порядок её осуществления?</h3></div>
	<div align="justify" class="entry">
	 <p>Легализация — это подтверждение соответствия документов законодательству государства их происхождения с целью их использования в международном общении. Она представляет собой свидетельствование подлинности подписи должностного лица и печати государственного учреждения.</p>
	<p>Документы, составленные на территории одного государства, принимаются к рассмотрению на территории другого государства при условии их легализации в установленном порядке, если иное не предусмотрено законодательством государств или положениями международных договоров, отменяющих процедуру легализации, участниками которых они являются.</p>
	<p>Консульские загранучреждения Республики Узбекистан  легализуют документы, составленные  на территории государств, которые входят в их консульский округ.</p>
	<p>Документы, составленные на территории Республики Узбекистан, подлежат легализации в Министерстве иностранных дел Республики Узбекистан.</p>
	<p>      В рамках стран СНГ действует Конвенция о правовой помощи и правовых отношениях по гражданским, семейным и уголовным делам, подписанная в г.Минске 22.01.1993 года (Армения, Беларусь, Казахстан, Кыргызстан, Молдова (с оговоркой), Российская Федерация, Таджикистан, Туркменистан, Узбекистан и Украина). В соответствии со статьей 13 вышеупомянутой Конвенции «документы,  которые на  территории одной  из Договаривающихся Сторон изготовлены  или засвидетельствованы  учреждением или  специально на то уполномоченным лицом в пределах их компетенции и по установленной форме и  скреплены гербовой  печатью, принимаются  на территориях других Договаривающихся Сторон без какого-либо специального удостоверения».</p>
