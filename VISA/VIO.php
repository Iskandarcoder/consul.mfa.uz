  <div class="title"><h3>9.Возможно ли произвести в Узбекистане  обмен выданных компетентными учреждениями иностранных  государств документов граждан Узбекистана, подтверждающих их гражданское  состояние?</h3></div>
	<div align="justify" class="entry">
	<p>Выданные компетентными учреждениями иностранных  государств документы граждан Узбекистана, подтверждающие их гражданское  состояние, могут быть обменены на гербовые свидетельства Республики Узбекистан.</p>
	<p>При этом до приезда в Узбекистан, граждане должны легализовать документы, подлежащие обмену в консульских загранучреждениях Республики Узбекистан за рубежом.</p>
