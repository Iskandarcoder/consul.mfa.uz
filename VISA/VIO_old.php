	<h3><a href="#"><span class="style1">4.Истребование документов</span></a></h3>
	<div class="style2">
		<p>
		Граждане Республики Узбекистан и иностранные граждане вправе обращаться как в архивные органы Республики Узбекистан, так и в дипломатические представительства и консульские учреждения Республики Узбекистан за границей для истребования необходимых документов. В тех случаях, когда нужный гражданину документ находится на территории Республики Узбекистан, применяется следующий порядок его истребования.</p>
<p>Заинтересованное лицо обращается в дипломатическое представительство или консульское учреждение в государстве своего пребывания и заполняет два экземпляра анкеты установленной формы на каждый истребуемый документ, а также оплачивает соответствующий консульский сбор.</p>
<p>Оформленный запрос направляется дипломатическим представительством (консульским учреждением) через КУ МИД Республики Узбекистан в соответствующие архивные учреждения на территории Республики Узбекистан. После завершения поиска архивные учреждения через КУ МИД Республики Узбекистан возвращают в дипломатическое представительство (консульское учреждение) истребованные документы. В тех случаях, когда разыскать документ не представляется возможным, архивное учреждение сообщает об этом заявителю также через КУ МИД Республики Узбекистан.</p>
<p><strong>Не подлежат истребованию,</strong> пересылке или вывозу за границу подлинники трудовых и пенсионных книжек, военные билеты.
  <strong>Не могут быть истребованы</strong> также повторные свидетельства о заключении брака, если он расторгнут, а также свидетельства о рождении на умерших. Свидетельства о рождении несовершеннолетних истребуются их законными представителями. Свидетельства о смерти могут быть истребованы родственниками умерших или юридическими лицами для официальных целей.</p>
<p>Практика свидетельствует, что осуществление запросов об истребовании из Республики Узбекистан документов об образовании (аттестатов, дипломов и выписок из них) осуществляется значительно эффективнее при личном обращении заинтересованных лиц непосредственно в учебные заведения. Необходимым условием является возмещение всех расходов, связанных с поиском личного дела в архивах, изготовлением дубликата документа, его заверением и пересылкой заявителю. Следует иметь в виду, что для истребования документов из-за границы требуется довольно длительное время.
	  </p>
