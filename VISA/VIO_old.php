<strong>Для оформления свидетельства о смерти гражданина Республики Узбекистан необходимы следующие документы:</strong><br>
 1.	Обращение заявителя (родственника, знакомого, представителей похоронных и других служб, и др.).<br>
2.	Оригинал справки медицинского учреждения о смерти с указанием даты, места и причины смерти или её заверенная копия.<br>
3.	Паспорт умершего или другой документ, подтверждающий его личность.		</p>
	</div>
	
<h3><a href="#"><span class="style1">4.Можно ли зарегистрировать брак в консульских загранучреждениях Республики Узбекистан за рубежом и что для этого нужно?</span> </a></h3>
	<div class="style2">
		<p>
В соответствии с законодательством Республики Узбекистан, регистрация  брака   гражданина  Республики  Узбекистан   с иностранным гражданином или лицом без гражданства производится  консулом в случаях, если заключение  брака признается законами того  государства, на  территории  которого  находится  консульское  учреждение,  а   также соглашением,  в  подписании  которого  участвовали  данное государство и Республика  Узбекистан,   или  если   это  соответствует   международным договорам.
<strong>Перечень необходимых документов для регистрации брака гражданина Республики Узбекистан (явка обоих вступающих в брак граждан обязательна)</strong>:<br>
1.	Заявление лиц, вступающих в брак (установленная форма заявления у консула).<br>
2.	Оригинал справки об отсутствии брака (жениха и невесты) или её копия, легализованная МИД Республики Узбекистан (для граждан Республики Узбекистан), МИД иностранного государства (для иностранных граждан).<br>
3.	Паспорта вступающих в брак.<br>
Оплата за консульские услуги и возмещение фактических расходов.
	  </p>
	</div>
<h3><a href="#"><span class="style1">5.Можно ли расторгнуть брак в консульских загранучреждениях Республики Узбекистан за рубежом и что для этого нужно?</span> </a></h3>
	<div class="style2">
	  <p>
Гражданин  Республики  Узбекистан,  проживающий  за    пределами территории  Республики  Узбекистан,   вправе    расторгнуть    брак    с проживающим  за  пределами  территории  Республики  Узбекистан  супругом (супругой),  независимо  от  его  гражданства,   в    суде    Республики Узбекистан.<br>
В случаях отсутствия между супругами имущественных споров и несовершеннолетних детей (в том числе усыновленных), то расторжение брака может производиться в консульских загранучреждениях Республики Узбекистан.
<strong>Перечень необходимых документов для регистрации расторжения брака гражданина Республики Узбекистан (явка обоих расторгающих брак граждан обязательна):</strong>
1.	Заявление о расторжении брака (установленная форма заявления у консула).<br>
2.	Паспорта расторгающих брак.<br>
3.	Свидетельство о заключении брака.<br>
4.	Оплата за консульские услуги и возмещение фактических расходов.<br>
Расторжение  брака  между  гражданами  Республики  Узбекистан  и иностранными гражданами  или  лицами  без  гражданства,  совершенное  за пределами    территории    Республики    Узбекистан    с     соблюдением законодательства соответствующего иностранного  государства,  признается действительным в Республике Узбекистан.		</p>
	</div>
<h3><a href="#"><span class="style1">6.Возможно ли поменять фамилию, имя или отчество находясь за рубежом и в каких случаях это делается? </span></a></h3>
	<div class="style2">
		<p>
Заявления   граждан   Республики   Узбекистан,  постоянно проживающих за  границей, рассматриваются  консулом. Заявление о перемене фамилии, имени и отчества подается заявителем по исполнении ему 16 лет (явка заявителя обязательна).
<strong>Заявления о перемене фамилии, имени и отчества рассматриваются в случае:</strong><br>
 - если фамилия, имя и отчество являются неблагозвучными;<br>
- если фамилия, имя и отчество не соответствуют национальности;<br>
- при возврате по желанию добрачной фамилии (во время нахождения в браке и после расторжения брака);<br>
- при возникновении желания избрать общую фамилию для семьи (если при вступлении в брак супруги не приняли общей фамилии);<br>
- по другим основаниям, если консул признал их уважительными.<br>
<strong>Перечень необходимых документов для перемены фамилии, имени и отчества:</strong><br>
1.	Заявление о перемене фамилии, имени и отчества (установленная форма заявления у консула).<br>
2.	Свидетельство о рождении заявителя.<br>
3.	Если заявитель  состоит в браке, то свидетельство заключении брака, если разведен – свидетельство о расторжении брака.
4.	Если заявитель имеет несовершеннолетних детей, то свидетельства о рождении детей.<br>
5.	Выписка из паспорта или другого документа, удостоверяющая личность заявителя.<br>
6.	Если заявитель является несовершеннолетним (от 16 до 18 лет)  или признан решением суда недееспособным, то заявление родителей либо опекуна или попечителя.<br>
7.	1 фотография (размером 3х4).<br>
8.	Для лиц, не достигших совершеннолетия (от 16 до 18 лет)  необходимо также письменное согласие родителей.
9.  Оплата за консульские услуги и возмещение фактических расходов.<br>
Если лицо, достигшее 16 лет, подает заявление о перемене фамилии, имени и отчества до получения паспорта или заменяющего его документа, подтверждающего личность, то к заявлению прилагаются свидетельство о рождении и копия записи акта о рождении.</p>
	</div>
<h3><a href="#"><span class="style1">7.Возможно ли внести изменения, исправления и дополнения в актовые записи находясь за рубежом и в каких случаях это делается?</span> </a></h3>
	<div class="style2">
	  <p>
Заявления  граждан  Республики  Узбекистан, находящихся за границей,  об   изменении,  исправлении,   дополнении  актовых   записей рассматриваются консульскими  учреждениями.<br>
<strong>Перечень необходимых документов для внесения изменений, исправлений и дополнений в актовые записи (явка заявителя обязательна).</strong><br>
 1.	Заявление  о внесении изменений, исправлений и дополнений в актовые записи (установленная форма заявления у консула).<br>
2.	Копии документов, подтверждающих обоснованность просьбы, в том числе копия паспорта, подлинники свидетельств, требующих изменения.<br>
<strong>3.	При необходимости другие документы, подтверждающие обоснованность просьбы при следующих случаях: <br>
</strong>
а) при наличии желания записать фамилию, имя и отчество в  целях соблюдения исторических и национальных традиций на основании статьи 15 Закона «О государственном языке Республики Узбекистан»;<br>
б) при установлении отцовства;<br>
в) при усыновлении;<br>
г) при перемене фамилии несовершеннолетнего ребенка по взаимному согласию родителей (а также при расторжении брака родителями) или по решению хокима;<br>
д) при изменении национальности;<br>
е) при подаче не состоящей в браке матерью заявления об изменении сведений об отце ребенка (до достижения ребенком 16-ти лет);<br>
ё) при признании решением суда недействительным установления отцовства или усыновления;<br>
ж) при изменении фамилии и отчества несовершеннолетнего ребенка в связи с переменой фамилии, имени и отчества родителей;<br>
з) при наличии орфографических ошибок и неточностей, а также при отсутствии записи о времени, месте рождения и отчестве;<br>
и) при изменении имени и фамилии ребенка, не достигшего 16 лет, если имя и фамилия были присвоены без учета желания родителей;<br>
к) если в записи акта имя записано сокращенно или в ласкательной форме;<br>
л) в других необходимых случаях.<br>
4.  Оплата за консульские услуги и возмещение фактических расходов.		</p>
	</div>
<h3><a href="#"><span class="style1">8.Признаются ли в Республике Узбекистан  браки, заключенные за пределами территории Республики Узбекистан? </span></a></h3>
	<div class="style2">
		<p>
Браки  между  гражданами  Республики  Узбекистан  и  браки между гражданами Республики  Узбекистан и  иностранными гражданами  или лицами без  гражданства,  заключенные  за  пределами  Республики  Узбекистан  с соблюдением  законодательства  государства,  на  территории которого они заключены,  признаются  действительными  в  Республике  Узбекистан, если отсутствуют предусмотренные статьей 16 Семейного Кодекса Республики Узбекистан обстоятельства, препятствующие заключению брака.
		</p>
	</div>
<h3><a href="#"><span class="style1">9.Возможно ли произвести в Узбекистане  обмен выданных компетентными учреждениями иностранных  государств документов граждан Узбекистана, подтверждающих их гражданское  состояние?</span> </a></h3>
	<div class="style2">
		<p>
Выданные компетентными учреждениями иностранных  государств документы граждан Узбекистана, подтверждающие их гражданское  состояние, могут быть обменены на гербовые свидетельства Республики Узбекистан.<br>
При этом до приезда в Узбекистан, граждане должны легализовать документы, подлежащие обмену в консульских загранучреждениях Республики Узбекистан за рубежом.
		</p>
