<p>Документы, составленные на территории одного государства, принимаются к рассмотрению на территории другого государства при условии их легализации в установленном порядке, если иное не предусмотрено законодательством государств или положениями международных договоров, отменяющих процедуру легализации, участниками которых они являются.
