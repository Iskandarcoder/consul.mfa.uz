    <div class="apost"><h3>APOSTIL QO`YISH VA HUJJATLARNI LEGALLASHTIRISH</h3>
<p> 2011 yilning mart oyida O`zbekiston Respublikasi Oliy Majlisi tomonidan "Xorijiy rasmiy hujjatlarni legallashtirish talabini bekor qiluvchi Konvensiyaga (Gaaga, 1961 yil 5 oktyabr) O`zbekiston Respublikasining qo`shilishi to`g`risida" Qonun qabul qilindi va Konvensiyaning depozitariysiga – Niderlandiyaning Tashqi ishlar vazirligiga – O`zbekiston Respublikasining ushbu Konvensiyaga qo`shilganligi to`g`risida xabarnoma jo`natildi.
<p> Konvensiyaning 11-moddasi va 12-moddasiga muvofiq, zaruriy tashkiliy jarayonlar rasmiylashtirilganidan so`ng, 2012 yilning 15 aprelidan Konvensiya O`zbekiston Respublikasi uchun kuchga kirdi.
<p> Konvensiyaga muvofiq, uning qoidalari Konvensiya depozitariysidan O`zbekiston Respublikasining Konvensiyaga qo`shilganligi to`g`risida xabarnoma olganidan so`ng olti oy ichida o`z e'tirozlarini bildirmagan boshqa ishtirokchi davlatlar bilan O`zbekiston Respublikasining munosabatlaridagina haqiqiydir.
<p> Hozirgi paytda dunyoning 100 dan oshiq davlatlari Konvensiyaning ishtirokchisidirlar.
<p> Gaaga Konvensiyasi – faqat uning ishtirokchilari bo`lmish davlatlar uchun xorijiy rasmiy hujjatlarni legallashtirish talabini bekor qiluvchi xalqaro Konvensiyadir.
<p> Konvensiya tomonidan bir davlatda tuzilib ikkinchi davlatga berilishi lozim bo`lgan rasmiy hujjatlarga qo`yiluvchi, konsullik legallashtirilishi jarayonining o`rnini bosuvchi, maxsus belgi (tamg`a) belgilangan, bu belgi apostil deyiladi.
<p> Konvensiyaning ishtirokchisi bo`lmish davlatda apostil orqali tasdiqlangan hujjatlar, Konvensiyaning boshqa ishtirokchisi bo`lmish davlatda hech qanday cheklovlarsiz qabul qilinishi lozim.
<p> Konvensiyaning ishtirokchi-davlatlari, ahdlashuvchi davlatlardan birining hududida tuzilib, boshqa ahdlashuvchi davlatlar hududida taqdim qilinishi lozim bo`lgan rasmiy hujjatlarning konsullik legallashtirilishi talabidan o`zaro voz kechadilar.
<p> Imzoning haqiqiyligini, hujjatni imzolovchi sifatida ish ko`rgan shaxs imzosining haqiqiyligini va hujjatni tasdiqlagan muhr yoki shtampning bosma izi haqiqiyligini tasdiqlash uchun zarur bo`lgan yagona rasmiyatchilik – bu apostil qo`yishdir.
<p> O`zbekiston Respublikasining uning Konvensiyaga qo`shilishi masalasida e'tiroz bildirgan Konvensiyaning ishtirokchi-davlatlari bilan, shuningdek, ushbu Konvensiyaning ishtirokchilari bo`lmagan davlatlar bilan munosabatlarida, avvalgidek, hujjatlarning konsullik legallashtirilishi amali qo`llaniladi.
<p> Xorijiy rasmiy hujjatlarni legallashtirish talabini bekor qiluvchi Konvensiyaning qoidalarini amalga oshirish maqsadida, 2011 yilning 5 iyulida O`zbekiston Respublikasi Prezidenti tomonidan PQ-1566-sonli "Xorijiy rasmiy hujjatlarni legallashtirish talabini bekor qiluvchi Konvensiya (Gaaga, 1961 yil 5 oktyabr) qoidalarini amalga oshirish choralari to`g`risida" Qarori qabul qilindi.
<p> Ushbu Qarorda O`zbekiston Respublikasida apostil qo`yish vakolatiga ega bo`lgan organlar belgilab berildi:
<p> O`zbekiston Respublikasi Adliya vazirligi — sudlar, adliya organlari va muassasalaridan chiqadigan rasmiy hujjatlarga;
<p> O`zbekiston Respublikasi Bosh prokuraturasi — prokuratura, tergov va surishtiruv organlaridan chiqadigan rasmiy hujjatlarga;
<p> O`zbekiston Respublikasi Vazirlar Mahkamasi huzuridagi Davlat test markazi — fan va ta`lim sohasi tashkilotlaridan chiqadigan rasmiy hujjatlarga;
<p> O`zbekiston Respublikasi Tashqi ishlar vazirligi — boshqa barcha rasmiy hujjatlarga.
<p> Konvensiya maqsadida rasmiy hujjatlar deganda quyidagilar tushuniladi:
<li>davlat yurisdiksiyasiga bo`ysunadigan organ yoki mansabdor shaxs tomonidan chiqariladigan hujjatlar, shu jumladan prokuratura, sud kotibi yoki sud ijrochisi tomonidan chiqariladigan hujjatlar;</li>
<li>ma'muriy hujjatlar (hususan, fuqarolik holati dalolatnomalarini qayd qilish guvohnomalari va b.);</li>
<li>notarial dalolatnomalar;</li>
<li>ro`yxatga olish kabi rasmiy qaydlar; ma'lum bir sanani tasdiqlovchi vizalar; notariusda tasdiqlanmagan hujjatdagi imzoni tasdiqlash.</li>
<p> Konvensiya quyidagilarga taalluqli emas:
<li>diplomatik yoki konsullik agentlari tomonidan amalga oshirilgan hujjatlar;</li>
<li>tijorat yoki bojxona operatsiyalariga to`g`ridan-to`g`ri aloqador bo`lgan ma'muriy hujjatlar.</li>
<p> Konvensiya, o`zining tom ma'nosida, shuningdek, norasmiy (ya'ni, davlat organlari tomonidan chiqarilmagan, hamda notarial tasdiqlanmagan) hujjatlarga ham taalluqli emasdir. Masalan, xorijiy firmalarning tijorat xatlariga apostil qo`yilmaydi.
<p> Apostilni qo`yish jarayoniga faqat Adliya vazirligining, Bosh prokuraturaning, TIV ning markaziy organlari hamda Davlat test markazi jalb qilinadi. O`zbekiston Respublikasining xorijiy konsullik muassasalari apostil qo`yish jarayonida ishtirok etmaydilar.
        <p> Ushbu masala bo`yicha qo`shimcha ma'lumotlarni xalqaro shaxsiy huquq bo`yicha Gaaga Konferensiyasining rasmiy <a href="www.hcch.net/index_en.php?act=conventions.text&cid=41">veb-sahifasidan</a> olishingiz mumkin.

<H3>HUJJATLARNI LEGALLASHTIRISH</H3>
<P>Konsullik legalizatsiyasi hujjat va aktlarda imzolarning haqiqiy ekanligini, hamda ularning istiqomat qilinayotgan davlat qonunlariga muvofiqligini belgilash va tasdiqlashdan iborat. Konsullik legalizatsiyasi hujjatlarning va aktlarning xalqaro muloqotda huquqiyligini tasdiqlaydi.</P>
<P>Konsullik legalizatsiyasi vazifalarini quyidagilar amalga oshiradi:</P>
<LI>Xorijda – O`zbekiston Respublikasi xorijdagi konsullik muassasalari;</LI>
<LI>O`zbekiston Respublikasida – O`zbekiston Respublikasi TIV Konsullik boshqarmasi.</LI>
<P>Konsul konsullik hududi vakolatli idoralari tomonidan tasdiqlangan yoki ushbu idoralar tomonidan chiqarilgan hujjatlarni legallashtirish uchun qabul qiladi.</P>
<P>O`zbekiston Respublikasi TIV Konsullik boshqarmasi, xorijiy tashkilotlar va muassasalar tomonidan qabul qilingan, hamda imzolari va muhrlari namunalari mavjud bo`lgan O`zbekiston Respublikasida akkreditatsiya qilingan konsullik muassasalari yoki xorijiy diplomatik vakolatxonalar tomonidan legallashtirilgan hujjatlar va aktlarni legallashtiradi.
<P>Konsullik boshqarmasi shuningdek O`zbekiston Respublikasi tashkilotlari va muassasalari tomonidan qabul qilinib, chet elda taqdim qilinishi lozim bo`lgan hujjatlar va aktlarni ham legallashtiradi.</P>
<P>O`zbekiston Respublikasi qonun hujjatlariga zid keladigan, yoki o`zining mazmuniga ko`ra davlat manfaatlariga ziyon keltirishi mumkin bo`lgan, yohud chet elda foydalanish uchun mo`ljallangan hujjatlarning rasmiylashtirilishini belgilovchi tartiblarni buzgan holda rasmiylashtirilgan, yoki fuqarolarning or-nomusiga putur yetkazadigan ma'lumotlardan iborat bo`lgan hujjatlar va aktlar legallashtirilmaydi.</P>
<P>Shuningdek quyidagi hujjatlarning asli va nusxalari legallashtirilmaydi: pasportlar, partiyalarga mansubligi to`g`risidagi hujjatlar, kasaba uyushmalari biletlari, mehnat daftarchalari, harbiy bilet, respublika miqyosida qo`llanishga mo`ljallangan ma'lumot haqidagi hujjat, haydovchilik guvohnomalari, alohida tabaqalarga va dinga mansubligi haqidagi hujjatlar, hamda fuqarolik munosabatlaridan chiqarilgan mulkka taalluqli boshqa hujjatlar.</P>
<P>Hujjatlarni legallashtirish borasida amalga oshirilgan harakatlar uchun manfaatdor yuridik va jismoniy shaxslardan O`zbekiston Respublikasi Prezidentining 2013 yil 24 dekabrdagi  PP - 2095-sonli qarorining 3- ilovasida belgilangan konsullik yig`imi va amalda qilingan xarajatlar uchun to`lov undiriladi.</P>
