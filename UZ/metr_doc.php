    <h3>TUG`ILGANLIK HAQIDAGI GUVOHNOMA RASMIYLASHTIRISH VA OTALIKNI BELGILASH TARTIBI</h3>
<p id='p1'>Agar ota-onalar rasmiy nikohlangan bo`lsalar, ulardan birontasi konsullikka tashrif buyurishi va quyidagi hujjatlarni taqdim etishi zarur:</p>
<li>Bola onasining yoki otasining arizasi;</li>
<li>Tibbiy muassasaning farzand tug`ilganligi to`g`risidagi ma’lumotnomasi asl nusxasi;</li>
<li>Bola ota-onasining pasportlari;</li>
<li>Bolaning ota-onasi nikohlangani to`g`risidagi guvohnoma yoki uning tasdiqlangan nusxasi;</li>
<li>Ariza beruvchi yozib qo`yilgan jo`nab ketish varag`ining tasdiqlangan nusxasi (MDH mamlakatlari uchun);</li>
<li>Agar bolaning ota-onasidan biri xorijiy davlat fuqarosi bo`lsa, bolaning xorijiy davlat fuqarosi bo`lgan otasining (yoki onasining) O`zbekiston Respublikasining tug`ilganlik guvohnomasini rasmiylashtirish, hamda O`zbekiston Respublikasi fuqaroligini olish uchun notarial tasdiqlangan roziligini ham taqdim etish shart.</li>

<p id='p1'>Otalikni belgilash</p>
<p>Bolaning onasi bilan nikohda bo`lmagan shaxsning otaligi, o`zini bolaning otasi deb tan olgan shaxs va onaning konsullik muassasasiga birgalikda topshirgan arizasiga binoan belgilanadi.</p>
<p>Agar tug`ilganlikni qayd qilish va otalikni belgilash bir paytda amalga oshirilayotgan bo`lsa, ota-onaning roziligi bilan shu vaqtning o`zida bolaga familiya beriladi, ota haqidagi ma’lumotlar esa berilgan arizaga muvofiq yoziladi. Bunda yolg`iz onaning arizasi talab etilmaydi.</p>
<p>Otalikni ishonchnomaga binoan belgilashga yo`l qo`yilmaydi.</p>

<p id='p1'>Agar ota-onalar fuqarolik nikohida turgan bo`lsalar (bolaga otalikni belgilangan holda), ota-onaning ikkalasi ham konsulxonaga kelishi shart:</p>
<li>Bola onasining yoki otasining arizasi;</li>
<li>Ota va onaning birgalikdagi otalikni belgilash to`g`risidagi arizasi (o`rnatilgan shakli konsuldan olinadi);</li>
<li>Tibbiy muassasaning farzand tug`ilganligi to`g`risidagi ma’lumotnomasi asl nusxasi;</li>
<li>Bola ota-onasining pasportlari;</li>
<li>Agar bolaning ota-onasidan biri xorijiy davlat fuqarosi bo`lsa, bolaning xorijiy davlat fuqarosi bo`lgan otasining (yoki onasining) O`zbekiston Respublikasining tug`ilganlik guvohnomasini rasmiylashtirish, hamda O`zbekiston Respublikasi fuqaroligini olish uchun notarial tasdiqlangan roziligini ham taqdim etish shart.</li>
<p id='p1'>Agar bolaning onasi yolg`iz bo`lsa (bolaga nisbatan otalik belgilanmagan holda), konsulxonaga ona kelishi shart:</p>
<li>Bola onasining arizasi (o`rnatilgan shakli konsuldan olinadi).</li>
<li>Tibbiy muassasaning farzand tug`ilganligi to`g`risidagi ma’lumotnomasi asl nusxasi.</li>
<li>Bola onasining pasporti.</li>

